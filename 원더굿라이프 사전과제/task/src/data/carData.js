// 차량 ID로 특정 차량 데이터를 찾는 함수 (예외처리 추가)
export const getCarById = (id) => {
  try {
    if (!id) {
      console.warn("getCarById: ID가 제공되지 않았습니다.");
      return null;
    }

    const car = cars.find((car) => car.id === id);

    if (!car) {
      console.warn(
        `getCarById: ID '${id}'에 해당하는 차량을 찾을 수 없습니다.`
      );
      return null;
    }

    // 차량 데이터 유효성 검사
    if (!car.fullName || !car.trims || !car.exterior || !car.interior) {
      console.error(`getCarById: 차량 '${id}'의 데이터가 불완전합니다.`, car);
    }

    return car;
  } catch (err) {
    console.error("getCarById 함수 실행 중 오류 발생:", err);
    return null;
  }
};

// 모든 차량 데이터를 반환하는 함수 (예외처리 추가)
export const getAllCars = () => {
  try {
    if (!Array.isArray(cars) || cars.length === 0) {
      console.warn("getAllCars: 차량 데이터가 없습니다.");
      return [];
    }
    return cars;
  } catch (err) {
    console.error("getAllCars 함수 실행 중 오류 발생:", err);
    return [];
  }
};
